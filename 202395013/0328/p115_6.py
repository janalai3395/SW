a=47 #num1변수에 정수 47 저장
b=18 #num2변수에 정수 18 저장

res1=a+b #res1 변수에 num1*num2결과 저장
res2=a-b #res2 변수에 num1/num2결과 저장
res3=a*b #res3 변수에 num1//num2결과 저장
res4=a/b #res4 변수에 num1%num2결과 저장
res5=a%b #res5 변수에 num1%num2결과 저장

print("47과 18의 합은 : " ,res1)
print("47과 18의 차는 : " ,res2)
print("47과 18의 곱은 : " ,res3)
print("47과 18의 나눗셈은 : " ,res4,res5)