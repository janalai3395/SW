for i in range(10): #반복조건
    print(i,end=' ') #반복조건이 참인동안 반복
print()
for i in range(1,10): #반복조건
    print(i,end=' ')
print()
for j in range(1,12,2): #반복조건
    print(j,end=' ') #홀수 출력
print()
for num in  range(10,0,-1): #반복조건
    print(num,end=' ')